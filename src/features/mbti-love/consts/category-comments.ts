/** CATEGORY_COMMENTS 전용 캐시 — 동일 (label, score) 쌍에 대해 세션 내 일관된 결과 */
const commentCache = new Map<string, string>();

/**
 * 점수 구간에 따라 해당 카테고리의 코멘트를 랜덤으로 반환한다.
 * 동일 (label, score) 쌍은 캐시되어 세션 내 일관성을 유지한다.
 */
export function getCategoryComment(label: string, score: number): string {
  const comments = CATEGORY_COMMENTS[label];
  if (!comments) return "";

  const cacheKey = `${label}:${score}`;
  const cached = commentCache.get(cacheKey);
  if (cached) return cached;

  let tierComments: string[];
  if (score >= 75) tierComments = comments[0];
  else if (score >= 55) tierComments = comments[1];
  else if (score >= 35) tierComments = comments[2];
  else tierComments = comments[3];

  const result =
    tierComments[Math.floor(Math.random() * tierComments.length)];
  commentCache.set(cacheKey, result);
  return result;
}
